// Command e2e_http is an HTTP/3-over-SCION end-to-end test for containerlab
// topologies generated by testgen. It bundles three subcommands in one binary:
//
//   - server: run an HTTP/3 server over SCION (runs inside an AS node);
//   - client: fetch a URL from such a server over SCION (runs inside an AS node);
//   - run:    the test driver — discover the lab and exercise every AS pair by
//     running the server/client subcommands inside the node containers via
//     `docker exec`.
package main

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
)

func main() {
	root := &cobra.Command{
		Use:           "e2e_http",
		Short:         "HTTP/3-over-SCION end-to-end test for containerlab topologies",
		SilenceErrors: true,
		SilenceUsage:  true,
	}
	root.AddCommand(serverCmd(), clientCmd(), runCmd())
	if err := root.Execute(); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %s\n", err)
		os.Exit(1)
	}
}
