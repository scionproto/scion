// Command await_connectivity waits for the beaconing process in a containerlab
// SCION topology (generated by testgen) to establish full connectivity.
//
// It is purpose-built for the containerlab topology: each AS's control service
// listens on its node's containerlab management address, reachable from the
// host over the management bridge, so the segments API is queried directly over
// HTTP. Discovery is driven by the generated gen/ directory (as_list.yml and
// the per-AS cs*.toml). It exits 0 once full connectivity is established, or
// non-zero at timeout.
package main

import (
	"context"
	"encoding/json"
	"flag"
	"fmt"
	"net/http"
	"os"
	"sort"
	"time"

	"github.com/scionproto/scion/testing/clab/e2e"
)

var (
	genDir     = flag.String("gen", "gen", "testgen output directory")
	quiet      = flag.Duration("quiet", 10*time.Second, "stay silent for this long before reporting")
	timeout    = flag.Duration("timeout", 60*time.Second, "give up after this long")
	connectTO  = flag.Duration("connect-timeout", 5*time.Second, "per-request HTTP timeout")
	interval   = flag.Duration("interval", time.Second, "polling interval")
	httpClient = &http.Client{}
)

func main() {
	flag.Parse()
	os.Exit(run())
}

func run() int {
	ases, err := e2e.LoadASes(*genDir)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error: %s\n", err)
		return 1
	}
	if len(ases) == 0 {
		fmt.Fprintln(os.Stderr, "Error: no ASes found")
		return 1
	}
	httpClient.Timeout = *connectTO

	deadline := time.Now().Add(*timeout)
	verboseAt := time.Now().Add(*quiet)
	for {
		missing := check(ases)
		if len(missing) == 0 {
			fmt.Println("full connectivity established")
			return 0
		}
		now := time.Now()
		if now.After(deadline) {
			fmt.Fprintf(os.Stderr, "timeout after %s, giving up:\n", *timeout)
			printMissing(os.Stderr, missing)
			return 1
		}
		if now.After(verboseAt) {
			fmt.Printf("waiting for connectivity:\n")
			printMissing(os.Stdout, missing)
		}
		time.Sleep(*interval)
	}
}

// check returns, per AS, the segments it is still waiting for. An empty result
// means full connectivity. A core AS waits for a (core-)segment originating at
// every other core AS; a non-core AS waits for at least one up-segment (a
// segment ending at it).
func check(ases []e2e.AS) map[string][]string {
	cores := make([]string, 0, len(ases))
	for _, a := range ases {
		if a.Core {
			cores = append(cores, a.IA)
		}
	}

	missing := map[string][]string{}
	for _, a := range ases {
		segs, err := fetchSegments(a.SegmentsURL)
		if err != nil {
			missing[a.IA] = []string{fmt.Sprintf("control service unreachable: %s", err)}
			continue
		}
		if a.Core {
			starts := map[string]bool{}
			for _, s := range segs {
				starts[s.StartISDAS] = true
			}
			for _, other := range cores {
				if other != a.IA && !starts[other] {
					missing[a.IA] = append(missing[a.IA], "core seg from "+other)
				}
			}
		} else {
			up := false
			for _, s := range segs {
				if s.EndISDAS == a.IA {
					up = true
					break
				}
			}
			if !up {
				missing[a.IA] = []string{"up segment"}
			}
		}
	}
	return missing
}

// segment is one entry of the control service segments API response.
type segment struct {
	StartISDAS string `json:"start_isd_as"`
	EndISDAS   string `json:"end_isd_as"`
}

func fetchSegments(url string) ([]segment, error) {
	ctx, cancel := context.WithTimeout(context.Background(), *connectTO)
	defer cancel()
	req, err := http.NewRequestWithContext(ctx, http.MethodGet, url, nil)
	if err != nil {
		return nil, err
	}
	resp, err := httpClient.Do(req)
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()
	if resp.StatusCode != http.StatusOK {
		return nil, fmt.Errorf("status %s", resp.Status)
	}
	var segs []segment
	if err := json.NewDecoder(resp.Body).Decode(&segs); err != nil {
		return nil, fmt.Errorf("decoding segments: %w", err)
	}
	return segs, nil
}

func printMissing(w *os.File, missing map[string][]string) {
	ias := make([]string, 0, len(missing))
	for ia := range missing {
		ias = append(ias, ia)
	}
	sort.Strings(ias)
	for _, ia := range ias {
		sort.Strings(missing[ia])
		fmt.Fprintf(w, "  %s: %v\n", ia, missing[ia])
	}
}
