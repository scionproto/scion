package main

import (
	"bytes"
	"errors"
	"flag"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"runtime"
	"strings"
)

const packageDir = "pkg/slayers/path/hummingbird"

var sourceFiles = map[string]string{
	"asm_amd64.s":  filepath.Join("src", "crypto", "internal", "fips140", "aes", "aes_amd64.s"),
	"asm_arm64.s":  filepath.Join("src", "crypto", "internal", "fips140", "aes", "aes_arm64.s"),
	"asm_ppc64x.s": filepath.Join("src", "crypto", "internal", "fips140", "aes", "aes_ppc64x.s"),
}

func main() {
	outDir := flag.String("out-dir", "", "directory to write generated files into")
	prefix := flag.String("prefix", "", "prefix to add to generated output filenames")
	check := flag.Bool("check", false, "verify generated files are up to date")
	flag.Parse()

	if err := run(config{
		check:  *check,
		outDir: *outDir,
		prefix: *prefix,
	}); err != nil {
		fmt.Fprintf(os.Stderr, "gen_hbird_aesasm: %v\n", err)
		os.Exit(1)
	}
}

type config struct {
	check  bool
	outDir string
	prefix string
}

func run(cfg config) error {
	dstDir := cfg.outDir
	if dstDir == "" {
		repoRoot, err := findRepoRoot()
		if err != nil {
			return err
		}
		dstDir = filepath.Join(repoRoot, packageDir)
	}
	goRoot, err := resolveGOROOT(defaultGoEnv)
	if err != nil {
		return err
	}

	for _, dstName := range orderedSourceNames() {
		srcRel := sourceFiles[dstName]
		srcPath := filepath.Join(goRoot, srcRel)
		dstPath := filepath.Join(dstDir, cfg.prefix+dstName)
		content, err := buildOutput(srcPath, srcRel, runtime.Version())
		if err != nil {
			return fmt.Errorf("%s -> %s: %w", srcRel, dstName, err)
		}
		if cfg.check {
			if err := checkFile(dstPath, content); err != nil {
				return err
			}
			continue
		}
		if err := writeIfChanged(dstPath, content); err != nil {
			return err
		}
	}
	return nil
}

func orderedSourceNames() []string {
	return []string{
		"asm_amd64.s",
		"asm_arm64.s",
		"asm_ppc64x.s",
	}
}

func findRepoRoot() (string, error) {
	wd, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for dir := wd; ; dir = filepath.Dir(dir) {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", errors.New("could not find repository root containing go.mod")
		}
	}
}

func resolveGOROOT(goEnv func() (string, error)) (string, error) {
	if goroot := firstValidGOROOT(runtime.GOROOT(), os.Getenv("GOROOT")); goroot != "" {
		return goroot, nil
	}
	goroot, err := goEnv()
	if err != nil {
		return "", fmt.Errorf("resolve GOROOT: %w", err)
	}
	if goroot == "" {
		return "", errors.New("resolve GOROOT: empty result from go env GOROOT")
	}
	if !isValidGOROOT(goroot) {
		return "", fmt.Errorf("resolve GOROOT: invalid path %q", goroot)
	}
	return goroot, nil
}

func firstValidGOROOT(candidates ...string) string {
	for _, goroot := range candidates {
		if isValidGOROOT(goroot) {
			return goroot
		}
	}
	return ""
}

func isValidGOROOT(goroot string) bool {
	if goroot == "" {
		return false
	}
	info, err := os.Stat(filepath.Join(goroot, "src"))
	return err == nil && info.IsDir()
}

var defaultGoEnv = func() (string, error) {
	out, err := exec.Command("go", "env", "GOROOT").Output()
	if err != nil {
		return "", err
	}
	return strings.TrimSpace(string(out)), nil
}

func buildOutput(srcPath, srcRel, goVersion string) ([]byte, error) {
	body, err := os.ReadFile(srcPath)
	if err != nil {
		return nil, fmt.Errorf("read source %q with %s: %w", srcPath, goVersion, err)
	}
	body = normalizeNewlines(body)
	header := renderHeader(srcRel, goVersion)
	return append(header, body...), nil
}

func renderHeader(srcRel, goVersion string) []byte {
	return []byte(fmt.Sprintf(`// Code generated by gen_hbird_aesasm. DO NOT EDIT.
// Source: %s
// Go version: %s

`, filepath.ToSlash(srcRel), goVersion))
}

func normalizeNewlines(content []byte) []byte {
	content = bytes.ReplaceAll(content, []byte("\r\n"), []byte("\n"))
	content = bytes.ReplaceAll(content, []byte("\r"), []byte("\n"))
	return content
}

func checkFile(path string, expected []byte) error {
	current, err := os.ReadFile(path)
	if err != nil {
		return fmt.Errorf("read destination %q: %w", path, err)
	}
	current = normalizeNewlines(current)
	if !bytes.Equal(current, expected) {
		return fmt.Errorf("%s is stale; run go generate ./%s", path, packageDir)
	}
	return nil
}

func writeIfChanged(path string, content []byte) error {
	current, err := os.ReadFile(path)
	if err == nil && bytes.Equal(normalizeNewlines(current), content) {
		return nil
	}
	if err != nil && !errors.Is(err, os.ErrNotExist) {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, content, 0o644)
}
